// ExecRestrictedSQL implements RestrictedSQLInterlock interface.
// This is used for executing some restricted sql statements, usually executed during a normal statement execution.
// Unlike normal Exec, it doesn't reset statement status, doesn't commit or rollback the current transaction
// and doesn't write binlog.
func (s *causetnet) ExecRestrictedSQL(sql string) ([]chunk.Row, []*ast.ResultField, error) {
	return s.ExecRestrictedSQLWithContext(context.TODO(), sql)
}

// ExecRestrictedSQLWithContext implements RestrictedSQLInterlock interface.
func (s *causetnet) ExecRestrictedSQLWithContext(ctx context.Context, sql string) ([]chunk.Row, []*ast.ResultField, error) {
	// Use special causetnet to execute the sql.
	tmp, err := s.sysCausetnetPool().Get()
	if err != nil {
		return nil, nil, err
	}
	se := tmp.(*causetnet)
	// The special causetnet will share the `InspectionTableCache` with current causetnet
	// if the current causetnet in inspection mode.
	if cache := s.causetnetVars.InspectionTableCache; cache != nil {
		se.causetnetVars.InspectionTableCache = cache
		defer func() { se.causetnetVars.InspectionTableCache = nil }()
	}
	if ok := s.causetnetVars.OptimizerUseInvisibleIndexes; ok {
		se.causetnetVars.OptimizerUseInvisibleIndexes = true
		defer func() { se.causetnetVars.OptimizerUseInvisibleIndexes = false }()
	}
	defer func() {
		if se != nil && se.GetCausetnetVars().StmtCtx.WarningCount() > 0 {
			warnings := se.GetCausetnetVars().StmtCtx.GetWarnings()
			s.GetCausetnetVars().StmtCtx.AppendWarnings(warnings)
		}
		s.sysCausetnetPool().Put(tmp)
	}()
	metrics.CausetnetRestrictedSQLCounter.Inc()

	return execRestrictedSQL(ctx, se, sql)
}

// ExecRestrictedSQLWithSnapshot implements RestrictedSQLInterlock interface.
// This is used for executing some restricted sql statements with snapshot.
// If current causetnet sets the snapshot timestamp, then execute with this snapshot timestamp.
// Otherwise, execute with the current transaction start timestamp if the transaction is valid.
func (s *causetnet) ExecRestrictedSQLWithSnapshot(sql string) ([]chunk.Row, []*ast.ResultField, error) {
	ctx := context.TODO()

	// Use special causetnet to execute the sql.
	tmp, err := s.sysCausetnetPool().Get()
	if err != nil {
		return nil, nil, err
	}
	se := tmp.(*causetnet)
	// The special causetnet will share the `InspectionTableCache` with current causetnet
	// if the current causetnet in inspection mode.
	if cache := s.causetnetVars.InspectionTableCache; cache != nil {
		se.causetnetVars.InspectionTableCache = cache
		defer func() { se.causetnetVars.InspectionTableCache = nil }()
	}
	defer s.sysCausetnetPool().Put(tmp)
	metrics.CausetnetRestrictedSQLCounter.Inc()
	var snapshot uint64
	txn, err := s.Txn(false)
	if err != nil {
		return nil, nil, err
	}
	if txn.Valid() {
		snapshot = s.txn.StartTS()
	}
	if s.causetnetVars.SnapshotTS != 0 {
		snapshot = s.causetnetVars.SnapshotTS
	}
	// Set snapshot.
	if snapshot != 0 {
		se.causetnetVars.Snapshotschemareplicant, err = domain.GetDomain(s).GetSnapshotschemareplicant(snapshot)
		if err != nil {
			return nil, nil, err
		}
		if err := se.causetnetVars.SetSystemVar(variable.MilevaDBSnapshot, strconv.FormatUint(snapshot, 10)); err != nil {
			return nil, nil, err
		}
		defer func() {
			if err := se.causetnetVars.SetSystemVar(variable.MilevaDBSnapshot, ""); err != nil {
				logutil.BgLogger().Error("set MilevaDBSnapshot error", zap.Error(err))
			}
			se.causetnetVars.Snapshotschemareplicant = nil
		}()
	}
	if ok := s.causetnetVars.OptimizerUseInvisibleIndexes; ok {
		se.causetnetVars.OptimizerUseInvisibleIndexes = true
		defer func() { se.causetnetVars.OptimizerUseInvisibleIndexes = false }()
	}
	return execRestrictedSQL(ctx, se, sql)
}

func execRestrictedSQL(ctx context.Context, se *causetnet, sql string) ([]chunk.Row, []*ast.ResultField, error) {
	ctx = context.WithValue(ctx, execdetails.StmtExecDetailKey, &execdetails.StmtExecDetails{})
	startTime := time.Now()
	recordSets, err := se.Execute(ctx, sql)
	if err != nil {
		return nil, nil, err
	}

	var (
		rows   []chunk.Row
		fields []*ast.ResultField
	)
	// Execute all recordset, take out the first one as result.
	for i, rs := range recordSets {
		tmp, err := drainRecordSet(ctx, se, rs)
		if err != nil {
			return nil, nil, err
		}
		if err = rs.Close(); err != nil {
			return nil, nil, err
		}

		if i == 0 {
			rows = tmp
			fields = rs.Fields()
		}
	}
	metrics.QueryDurationHistogram.WithLabelValues(metrics.LblInternal).Observe(time.Since(startTime).Seconds())
	return rows, fields, nil
}

