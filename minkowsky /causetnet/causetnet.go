func (s *causetnet) SetProcessInfo(sql string, t time.Time, command byte, maxExecutionTime uint64) {
	// If command == mysql.ComSleep, it means the SQL execution is finished. The processinfo is reset to SLEEP.
	// If the SQL finished and the causetnet is not in transaction, the current start timestamp need to reset to 0.
	// Otherwise, it should be set to the transaction start timestamp.
	// Why not reset the transaction start timestamp to 0 when transaction committed?
	// Because the select statement and other statements need this timestamp to read data,
	// after the transaction is committed. e.g. SHOW MASTER STATUS;
	var curTxnStartTS uint64
	if command != mysql.ComSleep || s.GetCausetnetVars().InTxn() {
		curTxnStartTS = s.causetnetVars.TxnCtx.StartTS
	}
	pi := util.ProcessInfo{
		ID:               s.causetnetVars.ConnectionID,
		DB:               s.causetnetVars.CurrentDB,
		Command:          command,
		Plan:             s.currentPlan,
		PlanExplainRows:  plannercore.GetExplainRowsForPlan(s.currentPlan),
		Time:             t,
		State:            s.Status(),
		Info:             sql,
		CurTxnStartTS:    curTxnStartTS,
		StmtCtx:          s.causetnetVars.StmtCtx,
		StatsInfo:        plannercore.GetStatsInfo,
		MaxExecutionTime: maxExecutionTime,
	}
	if s.causetnetVars.User != nil {
		pi.User = s.causetnetVars.User.Username
		pi.Host = s.causetnetVars.User.Hostname
	}
	s.processInfo.Store(&pi)
}

func (s *causetnet) ExecuteInternal(ctx context.Context, sql string) (recordSets []sqlexec.RecordSet, err error) {
	origin := s.causetnetVars.InRestrictedSQL
	s.causetnetVars.InRestrictedSQL = true
	defer func() {
		s.causetnetVars.InRestrictedSQL = origin
	}()
	return s.Execute(ctx, sql)
}

func (s *causetnet) Execute(ctx context.Context, sql string) (recordSets []sqlexec.RecordSet, err error) {
	if span := opentracing.SpanFromContext(ctx); span != nil && span.Tracer() != nil {
		span1 := span.Tracer().StartSpan("causetnet.Execute", opentracing.ChildOf(span.Context()))
		defer span1.Finish()
		ctx = opentracing.ContextWithSpan(ctx, span1)
		logutil.Eventf(ctx, "execute: %s", sql)
	}

	charsetInfo, collation := s.causetnetVars.GetCharsetInfo()
	parseStartTime := time.Now()
	stmtNodes, warns, err := s.ParseSQL(ctx, sql, charsetInfo, collation)
	if err != nil {
		s.rollbackOnError(ctx)

		// Only print log message when this SQL is from the user.
		// Mute the warning for internal SQLs.
		if !s.causetnetVars.InRestrictedSQL {
			logutil.Logger(ctx).Warn("parse SQL failed", zap.Error(err), zap.String("SQL", sql))
		}
		return nil, util.SyntaxError(err)
	}
	if len(stmtNodes) != 1 {
		return nil, errors.New("Execute() API doesn't support multiple statements any more")
	}
	durParse := time.Since(parseStartTime)
	s.GetCausetnetVars().DurationParse = durParse

	rs, err := s.ExecuteStmt(ctx, stmtNodes[0])
	if err != nil {
		s.causetnetVars.StmtCtx.AppendError(err)
	}
	for _, warn := range warns {
		s.causetnetVars.StmtCtx.AppendWarning(util.SyntaxWarn(warn))
	}
	if rs == nil {
		return nil, err
	}
	return []sqlexec.RecordSet{rs}, err
}

// Parse parses a query string to raw ast.StmtNode.
func (s *causetnet) Parse(ctx context.Context, sql string) ([]ast.StmtNode, error) {
	charsetInfo, collation := s.causetnetVars.GetCharsetInfo()
	parseStartTime := time.Now()
	stmts, warns, err := s.ParseSQL(ctx, sql, charsetInfo, collation)
	if err != nil {
		s.rollbackOnError(ctx)

		// Only print log message when this SQL is from the user.
		// Mute the warning for internal SQLs.
		if !s.causetnetVars.InRestrictedSQL {
			logutil.Logger(ctx).Warn("parse SQL failed", zap.Error(err), zap.String("SQL", sql))
		}
		return nil, util.SyntaxError(err)
	}

	durParse := time.Since(parseStartTime)
	s.GetCausetnetVars().DurationParse = durParse
	isInternal := s.isInternal()
	if isInternal {
		causetnetExecuteParseDurationInternal.Observe(durParse.Seconds())
	} else {
		causetnetExecuteParseDurationGeneral.Observe(durParse.Seconds())
	}
	for _, warn := range warns {
		s.causetnetVars.StmtCtx.AppendWarning(util.SyntaxWarn(warn))
	}
	return stmts, nil
}

func (s *causetnet) ExecuteStmt(ctx context.Context, stmtNode ast.StmtNode) (sqlexec.RecordSet, error) {
	if span := opentracing.SpanFromContext(ctx); span != nil && span.Tracer() != nil {
		span1 := span.Tracer().StartSpan("causetnet.ExecuteStmt", opentracing.ChildOf(span.Context()))
		defer span1.Finish()
		ctx = opentracing.ContextWithSpan(ctx, span1)
	}

	s.PrepareTxnCtx(ctx)
	err := s.loadCommonGlobalVariablesIfNeeded()
	if err != nil {
		return nil, err
	}

	s.causetnetVars.StartTime = time.Now()

	// Some executions are done in compile stage, so we reset them before compile.
	if err := Interlock.ResetContextOfStmt(s, stmtNode); err != nil {
		return nil, err
	}

	// Transform abstract syntax tree to a physical plan(stored in Interlock.ExecStmt).
	compiler := Interlock.Compiler{Ctx: s}
	stmt, err := compiler.Compile(ctx, stmtNode)
	if err != nil {
		s.rollbackOnError(ctx)

		// Only print log message when this SQL is from the user.
		// Mute the warning for internal SQLs.
		if !s.causetnetVars.InRestrictedSQL {
			logutil.Logger(ctx).Warn("compile SQL failed", zap.Error(err), zap.String("SQL", stmtNode.Text()))
		}
		return nil, err
	}
	durCompile := time.Since(s.causetnetVars.StartTime)
	s.GetCausetnetVars().DurationCompile = durCompile
	if s.isInternal() {
		causetnetExecuteCompileDurationInternal.Observe(durCompile.Seconds())
	} else {
		causetnetExecuteCompileDurationGeneral.Observe(durCompile.Seconds())
	}
	s.currentPlan = stmt.Plan

	// Execute the physical plan.
	logStmt(stmtNode, s.causetnetVars)
	recordSet, err := runStmt(ctx, s, stmt)
	if err != nil {
		if !ekv.ErrKeyExists.Equal(err) {
			logutil.Logger(ctx).Warn("run statement failed",
				zap.Int64("schemaVersion", s.causetnetVars.TxnCtx.SchemaVersion),
				zap.Error(err),
				zap.String("causetnet", s.String()))
		}
		return nil, err
	}
	return recordSet, nil
}

// runStmt executes the sqlexec.Statement and commit or rollback the current transaction.
func runStmt(ctx context.Context, se *causetnet, s sqlexec.Statement) (rs sqlexec.RecordSet, err error) {
	if span := opentracing.SpanFromContext(ctx); span != nil && span.Tracer() != nil {
		span1 := span.Tracer().StartSpan("causetnet.runStmt", opentracing.ChildOf(span.Context()))
		span1.LogKV("sql", s.OriginText())
		defer span1.Finish()
		ctx = opentracing.ContextWithSpan(ctx, span1)
	}
	se.SetValue(causetnetctx.QueryString, s.OriginText())
	if _, ok := s.(*Interlock.ExecStmt).StmtNode.(ast.DDLNode); ok {
		se.SetValue(causetnetctx.LastExecuteDDL, true)
	} else {
		se.ClearValue(causetnetctx.LastExecuteDDL)
	}

	sessVars := se.causetnetVars
	// Save origTxnCtx here to avoid it reset in the transaction retry.
	origTxnCtx := sessVars.TxnCtx
	err = se.checkTxnAborted(s)
	if err != nil {
		return nil, err
	}
	rs, err = s.Exec(ctx)
	sessVars.TxnCtx.StatementCount++
	if !s.IsReadOnly(sessVars) {
		// All the history should be added here.
		if err == nil && sessVars.TxnCtx.CouldRetry {
			GetHistory(se).Add(s, sessVars.StmtCtx)
		}

		// Handle the stmt commit/rollback.
		if se.txn.Valid() {
			if err != nil {
				se.StmtRollback()
			} else {
				err = se.StmtCommit(sessVars.StmtCtx.MemTracker)
			}
		}
	}
	if rs != nil {
		return &execStmtResult{
			RecordSet: rs,
			sql:       s,
			se:        se,
		}, err
	}

	err = finishStmt(ctx, se, err, s)

	// If it is not a select statement, we record its slow log here,
	// then it could include the transaction commit time.
	s.(*Interlock.ExecStmt).FinishExecuteStmt(origTxnCtx.StartTS, err == nil, false)
	return nil, err
}

// execStmtResult is the return value of ExecuteStmt and it implements the sqlexec.RecordSet interface.
// Why we need a struct to wrap a RecordSet and provide another RecordSet?
// This is because there are so many causetnet state related things that definitely not belongs to the original
// RecordSet, so this struct exists and RecordSet.Close() is overrided handle that.
type execStmtResult struct {
	sqlexec.RecordSet
	se  *causetnet
	sql sqlexec.Statement
}

func (rs *execStmtResult) Close() error {
	se := rs.se
	err := rs.RecordSet.Close()
	return finishStmt(context.Background(), se, err, rs.sql)
}

// rollbackOnError makes sure the next statement starts a new transaction with the latest schemareplicant.
func (s *causetnet) rollbackOnError(ctx context.Context) {
	if !s.causetnetVars.InTxn() {
		s.RollbackTxn(ctx)
	}
}

// PrepareStmt is used for executing prepare statement in binary protocol
func (s *causetnet) PrepareStmt(sql string) (stmtID uint32, paramCount int, fields []*ast.ResultField, err error) {
	if s.causetnetVars.TxnCtx.schemareplicant == nil {
		// We don't need to create a transaction for prepare statement, just get information schema will do.
		s.causetnetVars.TxnCtx.schemareplicant = domain.GetDomain(s).schemareplicant()
	}
	err = s.loadCommonGlobalVariablesIfNeeded()
	if err != nil {
		return
	}

	ctx := context.Background()
	inTxn := s.GetCausetnetVars().InTxn()
	// NewPrepareExec may need startTS to build the Interlock, for example prepare statement has subquery in int.
	// So we have to call PrepareTxnCtx here.
	s.PrepareTxnCtx(ctx)
	s.PrepareTSFuture(ctx)
	prepareExec := Interlock.NewPrepareExec(s, schemareplicant.Getschemareplicant(s), sql)
	err = prepareExec.Next(ctx, nil)
	if err != nil {
		return
	}
	if !inTxn {
		// We could start a transaction to build the prepare Interlock before, we should rollback it here.
		s.RollbackTxn(ctx)
	}
	return prepareExec.ID, prepareExec.ParamCount, prepareExec.Fields, nil
}

func (s *causetnet) preparedStmtExec(ctx context.Context,
	stmtID uint32, prepareStmt *plannercore.CachedPrepareStmt, args []types.CausetObjectQL) (sqlexec.RecordSet, error) {
	st, err := Interlock.CompileExecutePreparedStmt(ctx, s, stmtID, args)
	if err != nil {
		return nil, err
	}
	causetnetExecuteCompileDurationGeneral.Observe(time.Since(s.causetnetVars.StartTime).Seconds())
	logQuery(st.OriginText(), s.causetnetVars)
	return runStmt(ctx, s, st)
}

// cachedPlanExec short path currently ONLY for cached "point select plan" execution
func (s *causetnet) cachedPlanExec(ctx context.Context,
	stmtID uint32, prepareStmt *plannercore.CachedPrepareStmt, args []types.CausetObjectQL) (sqlexec.RecordSet, error) {
	prepared := prepareStmt.PreparedAst
	// compile ExecStmt
	is := schemareplicant.Getschemareplicant(s)
	execAst := &ast.ExecuteStmt{ExecID: stmtID}
	if err := Interlock.ResetContextOfStmt(s, execAst); err != nil {
		return nil, err
	}
	execAst.BinaryArgs = args
	execPlan, err := planner.OptimizeExecStmt(ctx, s, execAst, is)
	if err != nil {
		return nil, err
	}

	stmtCtx := s.GetCausetnetVars().StmtCtx
	stmt := &Interlock.ExecStmt{
		GoCtx:           ctx,
		schemareplicant: is,
		Plan:            execPlan,
		StmtNode:        execAst,
		Ctx:             s,
		OutputNames:     execPlan.OutputNames(),
		PsStmt:          prepareStmt,
	}
	compileDuration := time.Since(s.causetnetVars.StartTime)
	causetnetExecuteCompileDurationGeneral.Observe(compileDuration.Seconds())
	s.GetCausetnetVars().DurationCompile = compileDuration

	stmt.Text = prepared.Stmt.Text()
	stmtCtx.OriginalSQL = stmt.Text
	stmtCtx.InitSQLDigest(prepareStmt.NormalizedSQL, prepareStmt.SQLDigest)
	stmtCtx.SetPlanDigest(prepareStmt.NormalizedPlan, prepareStmt.PlanDigest)
	logQuery(stmt.OriginText(), s.causetnetVars)

	// run ExecStmt
	var resultSet sqlexec.RecordSet
	switch prepared.CachedPlan.(type) {
	case *plannercore.PointGetPlan:
		resultSet, err = stmt.PointGet(ctx, is)
		s.txn.changeToInvalid()
	case *plannercore.Update:
		s.PrepareTSFuture(ctx)
		stmtCtx.Priority = ekv.PriorityHigh
		resultSet, err = runStmt(ctx, s, stmt)
	default:
		prepared.CachedPlan = nil
		return nil, errors.Errorf("invalid cached plan type")
	}
	return resultSet, err
}

// IsCachedExecOk check if we can execute using plan cached in prepared structure
// Be careful for the short path, current precondition is ths cached plan satisfying
// IsPointGetWithPKOrUniqueKeyByAutoCommit
func (s *causetnet) IsCachedExecOk(ctx context.Context, preparedStmt *plannercore.CachedPrepareStmt) (bool, error) {
	prepared := preparedStmt.PreparedAst
	if prepared.CachedPlan == nil {
		return false, nil
	}
	// check auto commit
	if !s.GetCausetnetVars().IsAutocommit() {
		return false, nil
	}
	// check schema version
	is := schemareplicant.Getschemareplicant(s)
	if prepared.SchemaVersion != is.SchemaMetaVersion() {
		prepared.CachedPlan = nil
		return false, nil
	}
	// maybe we'd better check cached plan type here, current
	// only point select/update will be cached, see "getPhysicalPlan" func
	var ok bool
	var err error
	switch prepared.CachedPlan.(type) {
	case *plannercore.PointGetPlan:
		ok = true
	case *plannercore.Update:
		pointUpdate := prepared.CachedPlan.(*plannercore.Update)
		_, ok = pointUpdate.SelectPlan.(*plannercore.PointGetPlan)
		if !ok {
			err = errors.Errorf("cached update plan not point update")
			prepared.CachedPlan = nil
			return false, err
		}
	default:
		ok = false
	}
	return ok, err
}

// ExecutePreparedStmt executes a prepared statement.
func (s *causetnet) ExecutePreparedStmt(ctx context.Context, stmtID uint32, args []types.CausetObjectQL) (sqlexec.RecordSet, error) {
	s.PrepareTxnCtx(ctx)
	var err error
	s.causetnetVars.StartTime = time.Now()
	preparedPointer, ok := s.causetnetVars.PreparedStmts[stmtID]
	if !ok {
		err = plannercore.ErrStmtNotFound
		logutil.Logger(ctx).Error("prepared statement not found", zap.Uint32("stmtID", stmtID))
		return nil, err
	}
	preparedStmt, ok := preparedPointer.(*plannercore.CachedPrepareStmt)
	if !ok {
		return nil, errors.Errorf("invalid CachedPrepareStmt type")
	}
	Interlock.CountStmtNode(preparedStmt.PreparedAst.Stmt, s.causetnetVars.InRestrictedSQL)
	ok, err = s.IsCachedExecOk(ctx, preparedStmt)
	if err != nil {
		return nil, err
	}
	if ok {
		return s.cachedPlanExec(ctx, stmtID, preparedStmt, args)
	}
	return s.preparedStmtExec(ctx, stmtID, preparedStmt, args)
}

func (s *causetnet) DropPreparedStmt(stmtID uint32) error {
	vars := s.causetnetVars
	if _, ok := vars.PreparedStmts[stmtID]; !ok {
		return plannercore.ErrStmtNotFound
	}
	vars.RetryInfo.DroppedPreparedStmtIDs = append(vars.RetryInfo.DroppedPreparedStmtIDs, stmtID)
	return nil
}

func (s *causetnet) Txn(active bool) (ekv.Transaction, error) {
	if !active {
		return &s.txn, nil
	}
	if !s.txn.validOrPending() {
		return &s.txn, errors.AddStack(ekv.ErrInvalidTxn)
	}
	if s.txn.pending() {
		defer func(begin time.Time) {
			s.causetnetVars.DurationWaitTS = time.Since(begin)
		}(time.Now())
		// Transaction is lazy initialized.
		// PrepareTxnCtx is called to get a tso future, makes s.txn a pending txn,
		// If Txn() is called later, wait for the future to get a valid txn.
		if err := s.txn.changePendingToValid(); err != nil {
			logutil.BgLogger().Error("active transaction fail",
				zap.Error(err))
			s.txn.cleanup()
			s.causetnetVars.TxnCtx.StartTS = 0
			return &s.txn, err
		}
		s.causetnetVars.TxnCtx.StartTS = s.txn.StartTS()
		if s.causetnetVars.TxnCtx.IsPessimistic {
			s.txn.SetOption(ekv.Pessimistic, true)
		}
		if !s.causetnetVars.IsAutocommit() {
			s.causetnetVars.SetStatusFlag(mysql.ServerStatusInTrans, true)
		}
		s.causetnetVars.TxnCtx.CouldRetry = s.isTxnRetryable()
		s.txn.SetVars(s.causetnetVars.KVVars)
		if s.causetnetVars.GetReplicaRead().IsFollowerRead() {
			s.txn.SetOption(ekv.ReplicaRead, ekv.ReplicaReadFollower)
		}
	}
	return &s.txn, nil
}

// isTxnRetryable (if returns true) means the transaction could retry.
// If the transaction is in pessimistic mode, do not retry.
// If the causetnet is already in transaction, enable retry or internal SQL could retry.
// If not, the transaction could always retry, because it should be auto committed transaction.
// Anyway the retry limit is 0, the transaction could not retry.
func (s *causetnet) isTxnRetryable() bool {
	sessVars := s.causetnetVars

	// The pessimistic transaction no need to retry.
	if sessVars.TxnCtx.IsPessimistic {
		return false
	}

	// If retry limit is 0, the transaction could not retry.
	if sessVars.RetryLimit == 0 {
		return false
	}

	// If the causetnet is not InTxn, it is an auto-committed transaction.
	// The auto-committed transaction could always retry.
	if !sessVars.InTxn() {
		return true
	}

	// The internal transaction could always retry.
	if sessVars.InRestrictedSQL {
		return true
	}

	// If the retry is enabled, the transaction could retry.
	if !sessVars.DisableTxnAutoRetry {
		return true
	}

	return false
}

func (s *causetnet) NewTxn(ctx context.Context) error {
	if s.txn.Valid() {
		txnID := s.txn.StartTS()
		err := s.CommitTxn(ctx)
		if err != nil {
			return err
		}
		vars := s.GetCausetnetVars()
		logutil.Logger(ctx).Info("NewTxn() inside a transaction auto commit",
			zap.Int64("schemaVersion", vars.TxnCtx.SchemaVersion),
			zap.Uint64("txnStartTS", txnID))
	}

	txn, err := s.store.Begin()
	if err != nil {
		return err
	}
	txn.SetVars(s.causetnetVars.KVVars)
	if s.GetCausetnetVars().GetReplicaRead().IsFollowerRead() {
		txn.SetOption(ekv.ReplicaRead, ekv.ReplicaReadFollower)
	}
	s.txn.changeInvalidToValid(txn)
	is := domain.GetDomain(s).schemareplicant()
	s.causetnetVars.TxnCtx = &variable.TransactionContext{
		schemareplicant: is,
		SchemaVersion:   is.SchemaMetaVersion(),
		CreateTime:      time.Now(),
		StartTS:         txn.StartTS(),
		ShardStep:       int(s.causetnetVars.ShardAllocateStep),
	}
	return nil
}

