// runInBootstrapCausetnet create a special causetnet for bootstrap to run.
// If no bootstrap and storage is remote, we must use a little lease time to
// bootstrap quickly, after bootstrapped, we will reset the lease time.
// TODO: Using a bootstrap tool for doing this may be better later.
func runInBootstrapCausetnet(store ekv.Storage, bootstrap func(Causetnet)) {
	s, err := createCausetnet(store)
	if err != nil {
		// Bootstrap fail will cause program exit.
		logutil.BgLogger().Fatal("createCausetnet error", zap.Error(err))
	}

	s.SetValue(causetnetctx.Initing, true)
	bootstrap(s)
	finishBootstrap(store)
	s.ClearValue(causetnetctx.Initing)

	dom := domain.GetDomain(s)
	dom.Close()
	domap.Delete(store)
}

